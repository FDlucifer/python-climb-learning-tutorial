def myexpo(num1, num2):
    '''
    this function takes one number to the power of another number and returns the result!

    :param num1: this is the base
    :param num2: this is the exponent
    :return: the result of the calculation
    '''
    return num1 ** num2

print(myexpo(8, 2))
